import 'package:analyzer/dart/constant/value.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

List<String>? dartObjToStringList(DartObject? obj) {
  final a = obj?.toStringValue();
  if (a != null) {
    return [a];
  }
  final b = obj?.toListValue()?.map((e) => e.toStringValue()).nonNulls.toList();
  return b;
}
