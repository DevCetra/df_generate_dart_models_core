List<String>? dartObjToStringList(dynamic dartObj) {
  final a = dartObj?.toStringValue() as String?;
  if (a != null) {
    return [a];
  }
  final b = dartObj
      ?.toListValue()
      ?.map((dynamic e) => e.toStringValue())
      .nonNulls
      .toList() as List<String>?;
  return b;
}
